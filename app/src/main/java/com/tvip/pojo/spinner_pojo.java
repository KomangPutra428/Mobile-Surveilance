package com.tvip.surveylance.pojo;

public class spinner_pojo {
    private String id;
    private String keterangan;

    public spinner_pojo(String id, String keterangan) {
        this.id = id;
        this.keterangan = keterangan;

    }

    public String getId() {
        return id;
    }

    public String getKeterangan() {
        return keterangan;
    }
}
